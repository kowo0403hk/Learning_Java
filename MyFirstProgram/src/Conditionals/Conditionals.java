public class Conditionals {
  public static void main(String[] args) {
    int age = 18;

    if (age > 18) {
      System.out.println("You are an adult.");
    } else if (age == 18) {
      System.out.println("You are just 18!");
    } else {
      System.out.println("You are not an adult yet.");
    }
  }
}
